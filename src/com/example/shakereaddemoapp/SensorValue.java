package com.example.shakereaddemoapp;

public class SensorValue {

	private float x;
	private float y;
	private float z;
	/**
	 * @return x
	 */
	public float getX() {
		return x;
	}
	/**
	 * @param x セットする x
	 */
	public void setX(float x) {
		this.x = x;
	}
	/**
	 * @return y
	 */
	public float getY() {
		return y;
	}
	/**
	 * @param y セットする y
	 */
	public void setY(float y) {
		this.y = y;
	}
	/**
	 * @return z
	 */
	public float getZ() {
		return z;
	}
	/**
	 * @param z セットする z
	 */
	public void setZ(float z) {
		this.z = z;
	}
	
}
